def area(width, height):
    result = width * height
    return result

def subtract(num1, num2):
    result = num1 - num2
    return result

def add(num1, num2):
    result = num1 + num2
    return result


result = area(5, 6)
print(result)
result = area(10, 5)
print(result)
result = area(8, 9)
print(result)

result = subtract(6, 5)
print(result)
result = subtract(10, 5)
print(result)

result = add(6, 5)
print(result)
result = add(10, 5)
print(result)